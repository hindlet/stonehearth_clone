pub fn get_voxel_colour_uv(index: u8) -> [f32; 2]{
    let x = index % 16;
    let y = index / 16;

    let pos = [x as f32 / 15.0, y as f32 / 15.0];

    // println!("{:?}: ({:?}, {:?}), {:?}", index, x, y, pos);
    pos
}

